#include "libft.h"

char	*ft_strmap(const char *s, char (*f)(char))
{
	size_t	i;
	size_t	j;
	char	*str;

	i = 0;
	if (s && f)
	{
		j = ft_strlen((char*)s);
		if ((str = (char*)malloc(sizeof(*str) * j + 1)) == NULL)
			return (NULL);
		while (s[i])
		{
			str[i] = f(s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}

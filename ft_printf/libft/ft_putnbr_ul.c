#include "libft.h"

void	ft_putnbr_ul(unsigned long n)
{
	if (n >= 10)
	{
		ft_putnbr_ul(n / 10);
		ft_putnbr_ul(n % 10);
	}
	else
		ft_putchar(n + '0');
}
